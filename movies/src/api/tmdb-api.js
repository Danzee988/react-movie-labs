export const getUpcomingMovies = () => {
  return fetch(`https://api.themoviedb.org/3/movie/upcoming?language=en-US&page=1`)
    .then((res) => {
      if (!res.ok) {
        throw new Error(`Failed to fetch upcoming movies: ${res.status}`);
      }
      return res.json();
    })
    .then((json) => json.results);
};
